
public class Switch {

	public static void main(String[] args) {
		int a=10;
		switch(a){
			case 10 :
				System.out.println("10점");
			case 20 :
				System.out.println("20점"); break;
			case 30 :
				System.out.println("30점");
			case 40 :
				System.out.println("40점");
			default :
				System.out.println("0점");
			
		}
	}

}
